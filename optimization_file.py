# Node capacities (transactions per minute)
Y_d_tx1 = {1: 30, 2: 30, 3: 30, 4: 40, 5: 40, 6: 0}
Y_d_tx2 = {1: 30, 2: 30, 3: 30, 4: 40, 5: 40, 6: 60}

# Breach loss amounts ($)
r_conf = {n: 518376.5957 for n in N}  # Confidentiality breach # 2024_global cybersecurity threat: average losses
r_int = {n: 518376.5957 for n in N}   # Integrity breach
