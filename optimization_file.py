# Confidentiality breach probabilities (v_p^c)

η_conf = [0.555690/1000000, 0.517645/1000000]
ξ_conf = [45.844057/100, 45.709050/100]
w_conf = len(η_conf)

# Integrity breach probabilities (v_p^i)
η_int = [0.503994/1000000, 0.488700/1000000]
ξ_int = [42.140788/100, 41.603242/100]
w_int = len(η_int)

# Authentication breach probabilities (v_p^auth)
η_auth = [0.496404/1000000, 0.505040/1000000]
ξ_auth = [43.610619/100, 44.206732/100]
w_auth = len(η_auth)

# Availability breach probabilities (v_p^a) - also reduced
η_avail = [0.513798/1000000, 0.460665/1000000]
ξ_avail = [38.362751/100, 36.70018/100]
w_avail = len(η_avail)

# Stochastic parameters
mu_a, sigma_a = -0.0149925, 0.205583  # For ε_avail ~ LogNormal
E_conf = 0.991179  # Expected value of ε_conf
E_int = 0.991884   # Expected value of ε_int
E_auth = 0.991141  # Expected value of ε_auth

