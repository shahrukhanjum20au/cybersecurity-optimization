def get_beta_params(mean, std):
    """Calculate alpha and beta for Beta distribution"""
    var = std ** 2
    alpha = ((1 - mean) / var - 1 / mean) * mean ** 2
    beta_val = alpha * (1 / mean - 1)
    return max(0.1, alpha), max(0.1, beta_val)  #Ensures parameters are ≥ 0.1 for numerical stability

def get_linear_multiplier(control_id, max_cost, breach_type):
    """Returns linear probability scaling based on cost and breach type"""

    # Calculates a cost-based scaling factor
    cost_ratio = control_costs[control_id] / max_cost # Divides control cost by maximum cost and Returns ratio between 0 and 1 $$breach type is not used
    return cost_ratio

# Generate simulation data
