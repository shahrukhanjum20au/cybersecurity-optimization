# ======================

M = 100  # Number of scenarios
np.random.seed(42)  # For reproducibility
samples_avail = lognorm.rvs(s=sigma_a, scale=np.exp(mu_a), size=M)  # Generate ε_avail samples

# ======================
# 7. GUROBI MODEL FORMULATION
# ======================

try:
    # Initialize model
    model = gp.Model("SecurityControlAllocation")

    # =====================================
    # DECISION VARIABLES
    # =====================================

    # First-stage variables (security controls)
    x_S = model.addVars(S.keys(), vtype=GRB.BINARY, name="x_S")  # System-level
    x_NS = model.addVars([(l,d,j) for l in L for d in N_l[l] for j in NS.keys()],
                        vtype=GRB.BINARY, name="x_NS")  # Node-level

    # Auxiliary variables
    u_conf = model.addVars(N, lb=0, ub=100, name="u_conf")  # Breach probabilities
    u_int = model.addVars(N, lb=0, ub=100, name="u_int")
    u_auth = model.addVars(N, lb=0, ub=100, name="u_auth")
    C_total = model.addVars(N, lb=0, name="C_total")  # Total security cost per node

    # Second-stage variables
    D_breach = model.addVars(K, range(M), lb=0, ub=max(D_SysNormal.values()),
                            name="D_brech")
    capacity_reduction = model.addVars(range(M), name="capacity_reduction")
    effective_capacity = model.addVars(range(M), name="effective_capacity")
    total_processed = model.addVars(K, range(M), name="total_processed")

    # =====================================
    # OBJECTIVE FUNCTION
    # =====================================

    # 1. Control costs (first stage)
    control_costs = (
        gp.quicksum(C_S[i] * x_S[i] for i in S.keys()) +
        gp.quicksum(C_NS[j] * x_NS[l,d,j]
                   for l in L for d in N_l[l] for j in NS.keys())
    )

    # 2. Expected breach costs (using E[ε])
    breach_costs = (
        gp.quicksum(r_conf[d] * E_conf * u_conf[d] for d in N) +
        gp.quicksum(r_int[d] * E_int * u_int[d] for d in N) +
        gp.quicksum(r_auth[d] * E_auth * u_auth[d] for d in N)
    )

    # 3. Expected availability loss (SAA)
    avail_losses = (1/M) * gp.quicksum(
        C_avail[k] * (D_SysNormal[k] - D_breach[k,m]) * t_avail
        for k in K for m in range(M)
    )

    model.setObjective(control_costs + breach_costs + avail_losses, GRB.MINIMIZE)

    # =====================================
    # CONSTRAINTS
    # =====================================

    # Total security cost per node
    for d in N:
        model.addConstr(
            C_total[d] == (
                gp.quicksum(C_S[i] * x_S[i] for i in S.keys()) +
                gp.quicksum(C_NS[j] * x_NS[l,d,j]
                           for l in L for j in NS.keys() if d in N_l[l])
            ),
            name=f"total_cost_{d}"
        )

    # Piecewise linear breach probability functions
    for d in N:
        for m in range(w_conf):
            model.addConstr(
                u_conf[d] >= η_conf[m] * C_total[d] + ξ_conf[m],
                name=f"conf_{d}_{m}"
            )
        for m in range(w_int):
            model.addConstr(
                u_int[d] >= η_int[m] * C_total[d] + ξ_int[m],
                name=f"int_{d}_{m}"
            )
        for m in range(w_auth):
            model.addConstr(
                u_auth[d] >= η_auth[m] * C_total[d] + ξ_auth[m],
                name=f"auth_{d}_{m}"
            )

    # Scenario-specific capacity constraints
    for m in range(M):
        # Capacity reduction calculation
        capacity_reduction_tx1 = model.addVar(name=f"cap_red_tx1_{m}")
        capacity_reduction_tx2 = model.addVar(name=f"cap_red_tx2_{m}")
        model.addConstr(
            capacity_reduction_tx1 == gp.quicksum(
                Y_d_tx1[d] * (η_avail[0] * C_total[d] + ξ_avail[0]) * samples_avail[m]
                for d in N
            ),
            name=f"cap_red_tx1_{m}"
        )

        model.addConstr(
            capacity_reduction_tx2 == gp.quicksum(
                Y_d_tx2[d] * (η_avail[0] * C_total[d] + ξ_avail[0]) * samples_avail[m]
                for d in N
            ),
            name=f"cap_red_tx2_{m}"
        )

        effective_capacity_tx1 = sum(rho_l.values()) - capacity_reduction_tx1
        effective_capacity_tx2 = sum(rho_l.values()) - capacity_reduction_tx2

        # Transaction processing constraints with differentiated requirements
        for k in K:
            if k == 1:  # Tx1
                model.addConstr(
                    total_processed[k,m] == gp.quicksum(
                        D_breach[k,m] * pi_kl.get((k,l), 0) for l in L
                    ),
                    name=f"total_processed_tx1_{m}"
                )

                model.addConstr(
                    total_processed[k,m] <= effective_capacity_tx1,
                    name=f"capacity_tx1_{m}"
                )

                # for Tx1
                model.addConstr(
                    D_breach[k,m] <= D_SysNormal[k]* (effective_capacity_tx1 / (sum(rho_l.values()))),
                    name=f"cap_adj_tx1_{m}"
                )

            else:  # Tx2
                model.addConstr(
                    total_processed[k,m] == gp.quicksum(
                        D_breach[k,m] * pi_kl.get((k,l), 0) for l in L
                    ),
                    name=f"total_processed_tx2_{m}"
                )

                model.addConstr(
                    total_processed[k,m] <= effective_capacity_tx2,
                    name=f"capacity_tx2_{m}"
                )

                # for Tx2
                model.addConstr(
                    D_breach[k,m] <= D_SysNormal[k]* (effective_capacity_tx2 / (sum(rho_l.values()))),
                    name=f"cap_adj_tx2_{m}"
                )
        # Effective capacity calculation
        model.addConstr(
            effective_capacity[m] == sum(rho_l.values()) - capacity_reduction[m],
            name=f"effective_cap_{m}"
        )

    # =====================================
    # ADDITIONAL CONSTRAINTS FOR CONTROL SELECTION
    # =====================================

    # Constraint to ensure exactly one system-level control is selected
    model.addConstr(gp.quicksum(x_S[i] for i in S.keys()) == 1, name="system_control_selection")

    # Constraint to ensure exactly one node-level control is selected for each node
    for l in L:
        for d in N_l[l]:
            model.addConstr(gp.quicksum(x_NS[l,d,j] for j in NS.keys()) == 1,
                           name=f"node_control_selection_{d}")

    # =====================================
    # SOLVE THE MODEL
    # =====================================
    model.optimize()

    # =====================================
    # RESULTS PROCESSING
    # =====================================
    if model.status == GRB.OPTIMAL:
        print("STOCHASTIC OPTIMIZATION RESULTS")

        # Print security settings
        print("\nSystem-level security settings:")
        for i in sorted(S.keys()):
            print(f" x_S[{i}] = {int(x_S[i].x)}")

        for l in L:
            if l == 1:
                print("\nEdge Layer Nodes:")
            elif l == 2:
                print("\nFog Layer Nodes:")
            elif l == 3:
                print("\nCloud Layer Nodes:")

            for d in sorted(N_l[l]):
                for j in sorted(NS.keys()):
                    print(f" x_NS[{d},{j}] = {int(x_NS[l,d,j].x)}")

        # Print breach probabilities
        print("\nBREACH PROBABILITIES:")
        for d in N:
            print(f"\nNode {d}:")
            print(f"  Confidentiality: {u_conf[d].X/10000:.8f}")
            print(f"  Integrity: {u_int[d].X/10000:.8f}")
            print(f"  Authentication: {u_auth[d].X/10000:.8f}")

        # Print transaction rates by scenario
        print("\nTRANSACTION RATE VARIATION ACROSS SCENARIOS:")
        print(f"{'Scenario':<8} {'Capacity':<12} {'Tx1 Rate':<12} {'Tx2 Rate':<12}")
        for m in range(M):
           print(f"{m+1:<8} {samples_avail[m]:<12.4f}"
                  f"{D_breach[1,m].X:<12.2f} {D_breach[2,m].X:<12.2f}")

        # Calculate variation statistics
        tx1_rates = [D_breach[1,m].X for m in range(M)]
        tx2_rates = [D_breach[2,m].X for m in range(M)]

        print("\nTRANSACTION RATE STATISTICS:")
        print(f"{'Metric':<12} {'Tx1':<12} {'Tx2':<12}")
        print(f"{'Mean':<12} {np.mean(tx1_rates):<12.2f} {np.mean(tx2_rates):<12.2f}")
        print(f"{'Std Dev':<12} {np.std(tx1_rates):<12.2f} {np.std(tx2_rates):<12.2f}")
        print(f"{'Min':<12} {min(tx1_rates):<12.2f} {min(tx2_rates):<12.2f}")
        print(f"{'Max':<12} {max(tx1_rates):<12.2f} {max(tx2_rates):<12.2f}")

        # Print costs
        total_control_cost = sum(C_S[i]*x_S[i].x for i in S) + \
                           sum(C_NS[j]*x_NS[l,d,j].x for l in L for d in N_l[l] for j in NS)
        total_breach_cost = sum(r_conf[d] * E_conf * u_conf[d].X for d in N) + \
                           sum(r_int[d] * E_int * u_int[d].X for d in N) + \
                           sum(r_auth[d] * E_auth * u_auth[d].X for d in N)
        total_avail_cost = (1/M) * sum(C_avail[k] * (D_SysNormal[k] - D_breach[k,m].X) * t_avail
                          for k in K for m in range(M))

        print("\nCOST BREAKDOWN:")
        print(f"Control Costs: ${total_control_cost:,.2f}")
        print(f"Breach Costs: ${total_breach_cost:,.2f}")
  #      print(f"Availability Losses: ${total_avail_cost:,.2f}")
        print(f"\nTOTAL COST: ${model.ObjVal:,.2f}")

    else:
        print("\nOptimization failed with status:", model.status)
        if model.status == GRB.INFEASIBLE:
            print("Computing IIS to identify conflicting constraints...")
            model.computeIIS()
            model.write("infeasible_model.ilp")
            print("Infeasibility analysis written to 'infeasible_model.ilp'")

except gp.GurobiError as e:
    print(f"Gurobi error: {e}")
except Exception as e:
    print(f"Unexpected error: {e}")

