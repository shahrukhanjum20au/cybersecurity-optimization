"""# **Q-Q PLOTS**"""

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
import scipy.stats as stats

# Load the data
df = pd.read_csv('linear_breach_simulation_with_estimates.csv')

# Calculate ln(error) for each attribute
df['ln_error_Conf'] = np.log(df['Conf'] / df['Estimated_Conf'])
df['ln_error_Inte'] = np.log(df['Inte'] / df['Estimated_Inte'])
df['ln_error_Avai'] = np.log(df['Avai'] / df['Estimated_Avai'])
df['ln_error_Auth'] = np.log(df['Auth'] / df['Estimated_Auth'])

# Create Q-Q plots for each breach type
breach_types = ['Conf', 'Inte', 'Avai', 'Auth']
fig, axes = plt.subplots(2, 2, figsize=(12, 10))
axes = axes.flatten()

for i, breach in enumerate(breach_types):
    stats.probplot(df[f'ln_error_{breach}'], dist="norm", plot=axes[i], fit=False) # Removed fit=True
    axes[i].set_title(f'Q-Q Plot for ln(Error) - {breach}')
    axes[i].set_xlabel('Theoretical Quantiles')
    axes[i].set_ylabel('Sample Quantiles')

plt.tight_layout()
plt.savefig('qq_plots.png') # Save the figure
plt.show()